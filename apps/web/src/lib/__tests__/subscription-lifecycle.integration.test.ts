describe('SL-R1: atomic subscription creation', () => {
  it('subscribe_to_market_box_if_capacity RPC exists in database', async () => {
    // Verify the RPC function exists by querying pg_proc
    const { data, error } = await supabase
      .from('pg_catalog.pg_proc' as any)
      .select('proname')
      .eq('proname', 'subscribe_to_market_box_if_capacity')
      .limit(1)

    // If we can't query pg_catalog directly via PostgREST, verify via schema snapshot
    if (error) {
      const webRoot = path.resolve(__dirname, '..', '..', '..')
      const schema = fs.readFileSync(
        path.join(webRoot, '../../supabase/SCHEMA_SNAPSHOT.md'),
        'utf8'
      )
      expect(schema).toContain('subscribe_to_market_box_if_capacity')
    } else {
      expect(data?.length).toBeGreaterThan(0)
    }
  })
})

